#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	size_t dest_len;
	size_t i;

	dest_len = ft_strlen(s1);
	i = 0;
	while (s2[i] != '\0')
	{
		s1[dest_len + i] = s2[i];
		i++;
	}
	s1[dest_len + i] = '\0';
	return (s1);
}
