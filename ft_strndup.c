#include "libft.h"

char	*ft_strndup(const char *s1, size_t l)
{
	char	*s2;
	size_t	i;

	i = 0;
	s2 = ft_strnew(l);
	while (s1[i] && i < l)
	{
		s2[i] = s1[i];
		i++;
	}
	return (s2);
}
